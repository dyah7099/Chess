
public class Item {
	private String name;
	private int worth;
	private int rarity;
	private boolean isEquipped;
	
	public String getName()
	{
		return this.name;
	}
	
	public int getWorth()
	{
		return this.worth;
	}
	
	public int getRarity()
	{
		return this.rarity;
	}
	
	public boolean getIsEquipped()
	{
		return this.isEquipped;
	}
}
